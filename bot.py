def get_keyboard():
    names = list(SONGS.keys())
    buttons = []
    for i in range(0, len(names), 3):
        row = [InlineKeyboardButton(name, callback_data=f"song:{name}") for name in names[i:i+3]]
        buttons.append(row)
    return InlineKeyboardMarkup(buttons)


async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "🌸 Атыңды таңда / Выбери имя:",
        reply_markup=get_keyboard()
    )


async def send_song(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    name = query.data.split(":", 1)[1]
    song = SONGS.get(name)
    if not song:
        await query.message.reply_text("Песня не найдена")
        return

    await query.message.reply_audio(
        audio=song["file_id"],
        title=name,
        performer="",
        caption=f"🎵 *{name}*\n\n{song['lyrics']}",
        parse_mode="Markdown"
    )



def main():
    app = Application.builder().token(TOKEN).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(CallbackQueryHandler(send_song, pattern="^song:"))
    logger.info("Bot started!")
    app.run_polling(drop_pending_updates=True)


if __name__ == "__main__":
    main()